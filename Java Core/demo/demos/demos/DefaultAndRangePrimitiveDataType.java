package demos;

public class DefaultAndRangePrimitiveDataType {
	static int  a;
	static char ch;
	static long l;
	static short s;
	static byte b;
	static float f;
	static double d;
	static String st;
	static boolean bool;
	
	public static void main(String args[]){
		// below code gives default vales of primitive data type

		System.out.println("default int value is "+a);
		System.out.println("default char value is "+ch);
		System.out.println("default long value is "+l);
		System.out.println("default short value is "+s);
		System.out.println("default byte value is "+b);
		System.out.println("default float value is "+f);
		System.out.println("default double value is "+d);
		System.out.println("default String value is "+st);
		System.out.println("default boolean value is "+bool);
		//below code for range of primitive data type
		System.out.println("min int value = "+Integer.MIN_VALUE);
		System.out.println("max int value = "+Integer.MAX_VALUE);
		System.out.println("min long value = "+Long.MIN_VALUE);
		System.out.println("max long value = "+Long.MIN_VALUE);
		System.out.println("min short value = "+Short.MIN_VALUE);
		System.out.println("max short value = "+Short.MIN_VALUE);
		System.out.println("min byte value = "+Byte.MIN_VALUE);
		System.out.println("max byte value = "+Byte.MIN_VALUE);
		System.out.println("min float value = "+Float.MIN_VALUE);
		System.out.println("max float value = "+Float.MIN_VALUE);
		System.out.println("min double value = "+Double.MIN_VALUE);
		System.out.println("max double value = "+Double.MIN_VALUE);


	}

}
