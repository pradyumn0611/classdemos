package demos;

public class Loops {

	public static void main(Mystring[] args) {
		System.out.println("for loop");
		for(int i=0;i<10;i++)
		{
			System.out.println(i);
		}
		System.out.println("while loop");
		int temp=1;
		while(temp<=10)
		{
			System.out.println(temp);
			temp+=1;
		}
		System.out.println(" do while loop");
		do
		{
			System.out.println(temp);
			temp=31;
		}
		while(temp<30);
	}}